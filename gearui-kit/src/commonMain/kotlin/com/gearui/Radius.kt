package com.gearui

/**
 * GearUI 圆角 Token（纯静态常量）
 */
object Radius {

    /** 无圆角 */
    const val None = 0f

    /** 小圆角 - 3dp */
    const val Small = 3f

    /** 默认圆角 - 6dp */
    const val Default = 6f

    /** 中等圆角 - 9dp */
    const val Medium = 9f

    /** 大圆角 - 9dp */
    const val Large = 9f

    /** 超大圆角 - 12dp */
    const val ExtraLarge = 12f

    /** 圆形/胶囊 - 9999dp */
    const val Round = 9999f

    /** 圆形别名 */
    const val Circle = Round
}
