package com.gearui

/**
 * GearUI 字体 Token（纯静态常量）
 *
 * 格式：FontSize_LineHeight
 */
object Typography {

    // ============ Display 层级 ============

    /** Display Large - 64sp / 72sp */
    object DisplayLarge {
        const val fontSize = 64f
        const val lineHeight = 72f
    }

    /** Display Medium - 48sp / 56sp */
    object DisplayMedium {
        const val fontSize = 48f
        const val lineHeight = 56f
    }


    // ============ Headline 层级 ============

    /** Headline Large - 36sp / 44sp */
    object HeadlineLarge {
        const val fontSize = 36f
        const val lineHeight = 44f
    }

    /** Headline Medium - 28sp / 36sp */
    object HeadlineMedium {
        const val fontSize = 28f
        const val lineHeight = 36f
    }

    /** Headline Small - 24sp / 32sp */
    object HeadlineSmall {
        const val fontSize = 24f
        const val lineHeight = 32f
    }


    // ============ Title 层级 ============

    /** Title Extra Large - 20sp / 28sp */
    object TitleExtraLarge {
        const val fontSize = 20f
        const val lineHeight = 28f
    }

    /** Title Large - 18sp / 26sp */
    object TitleLarge {
        const val fontSize = 18f
        const val lineHeight = 26f
    }

    /** Title Medium - 16sp / 24sp */
    object TitleMedium {
        const val fontSize = 16f
        const val lineHeight = 24f
    }

    /** Title Small - 14sp / 22sp */
    object TitleSmall {
        const val fontSize = 14f
        const val lineHeight = 22f
    }


    // ============ Body 层级（最常用） ============

    /** Body Extra Large - 18sp / 26sp */
    object BodyExtraLarge {
        const val fontSize = 18f
        const val lineHeight = 26f
    }

    /** Body Large - 16sp / 24sp */
    object BodyLarge {
        const val fontSize = 16f
        const val lineHeight = 24f
    }

    /** Body Medium - 14sp / 22sp */
    object BodyMedium {
        const val fontSize = 14f
        const val lineHeight = 22f
    }

    /** Body Small - 12sp / 20sp */
    object BodySmall {
        const val fontSize = 12f
        const val lineHeight = 20f
    }

    /** Body Extra Small - 10sp / 16sp */
    object BodyExtraSmall {
        const val fontSize = 10f
        const val lineHeight = 16f
    }


    // ============ Label/Mark 层级 ============

    /** Mark Large - 16sp / 24sp */
    object MarkLarge {
        const val fontSize = 16f
        const val lineHeight = 24f
    }

    /** Mark Medium - 14sp / 22sp */
    object MarkMedium {
        const val fontSize = 14f
        const val lineHeight = 22f
    }

    /** Mark Small - 12sp / 20sp */
    object MarkSmall {
        const val fontSize = 12f
        const val lineHeight = 20f
    }

    /** Mark Extra Small - 10sp / 16sp */
    object MarkExtraSmall {
        const val fontSize = 10f
        const val lineHeight = 16f
    }


    // ============ Link 层级 ============

    /** Link Large - 16sp / 24sp */
    object LinkLarge {
        const val fontSize = 16f
        const val lineHeight = 24f
    }

    /** Link Medium - 14sp / 22sp */
    object LinkMedium {
        const val fontSize = 14f
        const val lineHeight = 22f
    }

    /** Link Small - 12sp / 20sp */
    object LinkSmall {
        const val fontSize = 12f
        const val lineHeight = 20f
    }


    // ============ 字重（Font Weight） ============

    object FontWeight {
        /** Regular - 400 */
        const val Regular = 400

        /** Medium - 500 */
        const val Medium = 500

        /** SemiBold - 600 */
        const val SemiBold = 600

        /** Bold - 700 */
        const val Bold = 700
    }
}
